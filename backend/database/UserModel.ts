import mongoose from 'mongoose'
const userSchema = new mongoose.Schema({
    //add id later on...
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        required: true,
        default: Date.now()
    },
    updatedAt: {
        type: Date,
        required: true,
        default: Date.now()
    }
})
export const UserModel = mongoose.model('UserModel', userSchema)